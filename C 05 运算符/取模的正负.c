# include "stdio.h"


int main(){

    // 被取模数正负决定余数正负

    printf("%d %% %d = %d\n", 32, 3, 32%3);   // 正数%正数, 为正数 2
    printf("%d %% %d = %d\n", -32, 3, -32%3);   // 负数%正数, 为负数 -2
    printf("%d %% %d = %d\n", 32, -3, 32%-3);   // 正数%负数, 为正数 2
    printf("%d %% %d = %d\n", -32, -3, -32%-3);   // 负数%负数, 为负数 -2


    return 0;
}